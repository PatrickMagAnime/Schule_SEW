    private void jBsuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsuchenActionPerformed
        
        for (int i = 0; i < PI.liste.size(); i++) {
            if (PI.liste.get(i).getVorname().equals(SuchTextFeld.getText())) {
                jInformationsArea.setText("Die person wurde auf Eintrag "+(i+1)+" gefunden.");
                break;
            }else{
                  if (PI.liste.get(i).getNachname().equals(SuchTextFeld.getText())) {
                jInformationsArea.setText("Die person wurde auf Eintrag "+(i+1)+" gefunden.");
                      break;
            }else{
                      if (PI.liste.get(i).getTelefonnummer().equals(SuchTextFeld.getText())) {
                jInformationsArea.setText("Die person wurde auf Eintrag "+(i+1)+" gefunden.");
                          break;
            }else{
                          if (PI.liste.get(i).getAdresse().equals(SuchTextFeld.getText())) {
                jInformationsArea.setText("Die person wurde auf Eintrag "+(i+1)+" gefunden.");
                              break;
            }else{
                           jInformationsArea.setText("Es konte niemand gefunden werden");   
                          }
                      }
                  }  
                    }
        }
        
    }//GEN-LAST:event_jBsuchenActionPerformed
 
