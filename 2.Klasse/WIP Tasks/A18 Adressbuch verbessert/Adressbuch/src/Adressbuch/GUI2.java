    private void jInformationsAreaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jInformationsAreaMouseClicked
    try {                                               
        //quelle: Stackoverflow
        Point pt = jInformationsArea.getMousePosition();
        int ss = jInformationsArea.getSelectionStart();
        String str = jInformationsArea.getText().substring(0, ss) + " ";
        int line = str.split("\n").length;
        
        // zeilen fÃ¤rben
        int startOfLine = jInformationsArea.getLineStartOffset(line-1);
        int endOfLine = jInformationsArea.getLineEndOffset(line-1);
        
        try {
           //macht line gelb
            DefaultHighlighter.DefaultHighlightPainter highlightPainter =
                    new DefaultHighlighter.DefaultHighlightPainter(Color.YELLOW);
            jInformationsArea.getHighlighter().addHighlight(startOfLine, endOfLine, highlightPainter);
        } catch (BadLocationException e) {
        }
        
       loeschen(false,startOfLine,endOfLine);
       //IDE wollte das so
    } catch (BadLocationException ex) {
            Logger.getLogger(GUI2.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }//GEN-LAST:event_jInformationsAreaMouseClicked

